    /* =========================================================================
     * Q3: Redis 分布式锁 vs ZooKeeper 分布式锁
     * ========================================================================= */

    /**
     * <pre>
     * Redis 锁（SET NX PX + Lua）：
     *   原理：SET key value NX PX ttl；Lua 脚本原子释放(value 校验)
     *   优点：高性能，简单易用，Redisson 封装完善(WatchDog 自动续期)
     *   缺点：主从切换可能丢失锁(AP 模型)；时钟依赖；RedLock 有争议
     *   适用：高并发、允许短暂锁丢失的场景（如防止缓存击穿）
     *
     * ZooKeeper 锁（临时顺序节点 + Watch）：
     *   原理：创建 /lock/0000000001 临时顺序节点，最小序号获锁，Watch 前节点
     *   优点：强一致(CP)，自动释放(会话断开→临时节点删除)，公平锁
     *   缺点：性能较低(过半写入)；羊群效应(需优化)
     *   适用：对一致性要求高、并发适中的场景(如定时任务调度)
     *
     * 对比关键点：
     *   - Redis 是 AP、ZK 是 CP
     *   - Redis 性能约是 ZK 的 10x
     *   - Redis 锁存在主从切换丢失风险，ZK 不会
     *   - ZK 天然支持公平锁和可重入
     * </pre>
     */
