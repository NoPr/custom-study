    /* =========================================================================
     * Q2: 2PC vs TCC vs Saga 怎么选？
     * ========================================================================= */

    /**
     * <pre>
     * 2PC (XA)：
     *   - 适用：传统单体拆分，少量资源，同步强一致
     *   - 优点：对业务无侵入，标准协议
     *   - 缺点：同步阻塞，协调者单点，性能差，不适合高并发
     *
     * TCC (Try-Confirm-Cancel)：
     *   - 适用：资金/积分等核心业务，需要强一致且高并发
     *   - 优点：不锁资源，性能好，最终一致性
     *   - 缺点：业务侵入大，需编写 Confirm/Cancel 逻辑，开发成本高
     *
     * Saga：
     *   - 适用：长事务，跨多个微服务，异构系统
     *   - 优点：异步、无锁、高可用，适合长流程
     *   - 缺点：缺乏隔离性，需补偿逻辑，可能读到脏数据
     *
     * 选型建议：
     *   一致性要求高 + 并发高 → TCC
     *   长流程 + 异步解耦 → Saga
     *   传统单体拆分 + 对性能要求不高 → 2PC
     *   最终一致性 + 异步 → MQ 可靠消息
     * </pre>
     */
