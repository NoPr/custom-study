    /* =========================================================================
     * Q4: 分布式系统幂等性怎么保证？
     * ========================================================================= */

    /**
     * <pre>
     * 幂等：同一个操作执行多次，结果和执行一次相同。
     *
     * 方案一：数据库唯一索引（最常用）
     *   - 利用唯一约束，INSERT 重复时抛 DuplicateKeyException
     *   - 适用：创建类操作（订单、用户等）
     *
     * 方案二：Token 机制（防重复提交）
     *   - 先 GET /token → 提交时带 token，服务端校验并删除 token
     *   - 适用：表单提交、支付
     *
     * 方案三：状态机（业务状态约束）
     *   - 定义状态流转（CREATED→PAID→SHIPPED），已到终态拒绝操作
     *   - 适用：订单状态流转、审批流程
     *
     * 方案四：乐观锁（版本号）
     *   - UPDATE ... WHERE version = ?
     *   - 适用：并发更新
     *
     * 方案五：MQ 消费幂等
     *   - 消费端记录 msg_id，已消费则跳过
     *   - 适用：消息队列消费
     * </pre>
     */
